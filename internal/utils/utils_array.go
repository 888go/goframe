package utils


import (
	"reflect"
	)
// IsArray 检查给定的值是否为数组或切片。
// 注意，该功能内部使用了 reflect 实现。
func IsArray(value interface{}) bool {
	rv := reflect.ValueOf(value)
	kind := rv.Kind()
	if kind == reflect.Ptr {
		rv = rv.Elem()
		kind = rv.Kind()
	}
	switch kind {
	case reflect.Array, reflect.Slice:
		return true
	default:
		return false
	}
}
